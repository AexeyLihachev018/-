import json
import requests
from config import OPENROUTER_API_KEY, OPENROUTER_MODEL, OPENROUTER_BASE_URL

EXTRACTION_PROMPT = """Ты анализируешь питч-презентацию стартапа на русском языке.
На изображениях — слайды или страницы питча.

ВАЖНО: Информация может быть НЕ подписана явным заголовком. Ищи её по смыслу:
- "проблема" — слайд/абзац где описывается боль рынка, что не так сейчас, потери клиентов
- "решение" — слайд/абзац где описывается как продукт эту боль устраняет, подход компании
- "целевая_аудитория" — кто покупает, для кого сделан продукт (может быть в слайде рынка или продукта)

Внимательно прочитай КАЖДУЮ страницу. Не пропускай информацию.

Верни ТОЛЬКО валидный JSON без markdown-обёртки, без пояснений:

{
  "название_проекта": "название стартапа или проекта",
  "сфера_индустрия": "сфера деятельности, индустрия (например: FinTech, EdTech, HealthTech, B2B SaaS и т.д.)",
  "стадия": "стадия проекта: pre-seed / seed / раунд A / MVP / и т.д.",
  "проблема": "какую проблему решает проект — ищи по смыслу даже без заголовка ПРОБЛЕМА",
  "решение": "предлагаемое решение — ищи по смыслу даже без заголовка РЕШЕНИЕ",
  "продукт": "что за продукт, как работает, ключевые функции или отличия",
  "целевая_аудитория": "целевые пользователи или клиенты — может быть указано в разделе рынка или продукта",
  "рынок": "объём рынка — TAM/SAM/SOM если указан, иначе общее описание рынка",
  "бизнес_модель": "как монетизируется: подписка / транзакции / лицензии / и т.д.",
  "тракшн_метрики": "текущие показатели: пользователи, выручка, MRR, рост, клиенты",
  "команда": "участники команды: имена и роли (например: Иван Иванов — CEO, Мария Петрова — CTO)",
  "инвестиции_запрос": "запрашиваемая сумма инвестиций",
  "бюджет_на_что": "на что планируется потратить инвестиции — основные статьи",
  "контакты": "email, телефон, telegram, сайт — всё что указано",
  "оценка_привлекательности": "число от 1 до 10 — общая инвест-привлекательность питча. 1-3: слабый питч, 4-6: средний, 7-8: сильный, 9-10: исключительный. Оценивай по: чёткость проблемы и решения, размер рынка, команда, трекшн, реалистичность запроса",
  "комментарий_инвестора": "2-3 предложения: сильные стороны питча и главные риски или вопросы к команде"
}

Правила:
- Ищи информацию по смыслу, не только по заголовкам
- Если информация действительно отсутствует — пиши "не указано"
- Все ответы на русском языке
- Каждое поле не более 3 предложений, будь конкретен
- Не придумывай — только то, что есть в документе
- Для оценки_привлекательности верни ТОЛЬКО число (например: 7), без пояснений в этом поле
- Верни ТОЛЬКО JSON, ничего больше
"""


def extract_pitch_data(images_b64: list[str]) -> dict:
    """Отправляет изображения в OpenRouter и возвращает структурированные данные питча."""
    content = [{"type": "text", "text": EXTRACTION_PROMPT}]

    for img_b64 in images_b64:
        content.append({
            "type": "image_url",
            "image_url": {"url": f"data:image/png;base64,{img_b64}"}
        })

    response = requests.post(
        f"{OPENROUTER_BASE_URL}/chat/completions",
        headers={
            "Authorization": f"Bearer {OPENROUTER_API_KEY}",
            "Content-Type": "application/json",
        },
        json={
            "model": OPENROUTER_MODEL,
            "messages": [{"role": "user", "content": content}],
            "temperature": 0.1,
        },
        timeout=180,
    )
    response.raise_for_status()

    raw = response.json()["choices"][0]["message"]["content"].strip()

    # Убираем markdown-обёртку если модель всё же добавила
    if raw.startswith("```"):
        lines = raw.split("\n")
        raw = "\n".join(lines[1:])
        raw = raw.rsplit("```", 1)[0]

    return json.loads(raw.strip())
